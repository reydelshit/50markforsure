    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        String apno = idno.getText();
        String pmed = Tfmed.getText();
        String asched = (String) sched.getSelectedItem();
        String astaff = (String) staff.getSelectedItem();
        String anote = (String) note.getSelectedItem();
    

        try {
            pst = conn.prepareStatement("INSERT INTO appointments(appno, medication, appointment, staff, note) VALUES (?,?,?,?,?)");
            pst.setString(1, apno);
            pst.setString(2, pmed);
            pst.setString(3, asched);
            pst.setString(4, astaff);
            pst.setString(5, anote);
          
            

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Appointment Added");

            idno.setText("PX000");
            Tfmed.setText("");
            sched.setSelectedItem("DROP TO SELECT");
            staff.setSelectedItem("DROP TO SELECT");
            note.setSelectedItem("DROP TO SELECT");
            Tfmed.requestFocus();

            showapptbl();

        } catch (SQLException ex) {
            Logger.getLogger(APPOINTMENTS.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

       
    
