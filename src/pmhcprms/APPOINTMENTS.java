       public void AutoID()
       {
        try {
            Statement s = conn.createStatement();
            rs = s.executeQuery("SELECT MAX(patientno) FROM patient_records");
            rs.next();
            rs.getString("MAX(patientno)");
            
            if(rs.getString("MAX(patientno)")== null){
                idno.setText("PX001");
            }
            else
            {
                long id=Long.parseLong(rs.getString("MAX(patientno)").substring(2,rs.getString("MAX(patientno)").length()));
                id++;
                
                idno.setText("PX"+ String.format("%03d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MANAGEPX.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
       
       public void showapptbl(){
          
            try {
                pst=conn.prepareStatement("SELECT * FROM appointments ORDER BY appno ASC");
                rs=pst.executeQuery();
                ResultSetMetaData rsmd = rs.getMetaData();
                int c;
                c = rsmd.getColumnCount();
                
                DefaultTableModel model = (DefaultTableModel)apptbl.getModel();
                model.setRowCount(0);
                
                while(rs.next() )
              {
                    Vector column  = new Vector(); 
                    
                    for(int i = 1;i<=c;i++){
                        column.add(rs.getString("appno"));
                        column.add(rs.getString("medication"));
                        column.add(rs.getString("appointment"));
                        column.add(rs.getString("staff"));
                        column.add(rs.getString("note"));
                    }
                    model.addRow(column);
                }
            } catch (SQLException ex) {
                Logger.getLogger(APPOINTMENTS.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
       
