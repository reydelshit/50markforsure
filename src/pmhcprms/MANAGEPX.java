            Tflastname.setText("");
            Tfaddress.setText("");
            Tfcellphone.setText("");
            Tfdiagnosis.setSelectedItem(null);
            sex.setSelectedItem("Select Sex");
            Tfage.setText("");
            Tfname.requestFocus();
            showtbl();
