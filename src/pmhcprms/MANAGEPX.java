    public void AutoID() {
        try {
            Statement s = conn.createStatement();
            rs = s.executeQuery("SELECT MAX(patientno) FROM patient_records");
            rs.next();
            rs.getString("MAX(patientno)");

            if (rs.getString("MAX(patientno)") == null) {
                idno.setText("PX001");
            } else {
                long id = Long.parseLong(rs.getString("MAX(patientno)").substring(2, rs.getString("MAX(patientno)").length()));
                id++;

                idno.setText("PX" + String.format("%03d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MANAGEPX.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void showtbl() {

        try {
            pst = conn.prepareStatement("SELECT * FROM patient_records ORDER BY patientno ASC");
            rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c;
            c = rsmd.getColumnCount();

            DefaultTableModel model = (DefaultTableModel) patientTable.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector column = new Vector();

                for (int i = 1; i <= c; i++) {
                    column.add(rs.getString("patientno"));
                    column.add(rs.getString("name"));
                    column.add(rs.getString("lastname"));
                    column.add(rs.getString("address"));
                    column.add(rs.getString("number"));
                    column.add(rs.getString("diagnosis"));
                    column.add(rs.getString("sex"));
                    column.add(rs.getString("age"));
                }
                model.addRow(column);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MANAGEPX.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

