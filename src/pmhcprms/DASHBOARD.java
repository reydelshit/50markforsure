    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
          DefaultTableModel tblmodel = (DefaultTableModel)Ptable.getModel();
        String show = search.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(tblmodel);
        Ptable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(show));
    }//GEN-LAST:event_searchKeyReleased

