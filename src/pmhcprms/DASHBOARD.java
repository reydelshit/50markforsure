public void showtbl(){
          
            try {
                pst=conn.prepareStatement("SELECT * FROM patient_records ORDER BY patientno ASC");
                rs=pst.executeQuery();
                ResultSetMetaData rsmd = rs.getMetaData();
                int c;
                c = rsmd.getColumnCount();
                
                DefaultTableModel model = (DefaultTableModel)Ptable.getModel();
                model.setRowCount(0);
                
                while(rs.next() )
              {
                    Vector column  = new Vector(); 
                    
                    for(int i = 1;i<=c;i++){
                        column.add(rs.getString("patientno"));
                        column.add(rs.getString("name"));
                        column.add(rs.getString("lastname"));
                        column.add(rs.getString("address"));
                        column.add(rs.getString("number"));
                        column.add(rs.getString("diagnosis"));
                    }
                    model.addRow(column);
                }
            } catch (SQLException ex) {
                Logger.getLogger(MANAGEPX.class.getName()).log(Level.SEVERE, null, ex);
            }

       
       
       }
    

